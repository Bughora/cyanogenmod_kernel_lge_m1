#ifndef __LINUX_RT5058_REGULATOR_H
#define __LINUX_RT5058_REGULATOR_H

#include <linux/mfd/rt5058/rt5058.h>

#define RT5058_PMIC_USE_NESTED_IRQ	(0)

#define RT5058_REGULATOR_SHIFT	0

#define RT5058_REGULATOR_REG0	RT5058_REG_BUCK1VOUT
#define RT5058_REGULATOR_REG1	RT5058_REG_LDO1VOUT
#define RT5058_REGULATOR_REG2	RT5058_REG_LDO2VOUT
#define RT5058_REGULATOR_REG3	RT5058_REG_LDO3VOUT
#define RT5058_REGULATOR_REG4	RT5058_REG_SLDO1VOUT
#define RT5058_REGULATOR_REG5	RT5058_REG_SLDO2VOUT

#define RT5058_BUCK_MASK	0x1f
#define RT5058_LDO_MASK		0x1f
#define RT5058_SLDO_MASK	0x03

#define RT5058_REGULATOR_MASK0	RT5058_BUCK_MASK
#define RT5058_REGULATOR_MASK1	RT5058_LDO_MASK
#define RT5058_REGULATOR_MASK2	RT5058_LDO_MASK
#define RT5058_REGULATOR_MASK3	RT5058_LDO_MASK
#define RT5058_REGULATOR_MASK4	RT5058_SLDO_MASK
#define RT5058_REGULATOR_MASK5	RT5058_SLDO_MASK

#define RT5058_REGULATOR_EN_MASK	0x80
#define RT5058_PMIC_IRQMASK		0x10

#define RT5058_REGULATOR_EN_REG0	RT5058_REGULATOR_REG0
#define RT5058_REGULATOR_EN_REG1	RT5058_REGULATOR_REG1
#define RT5058_REGULATOR_EN_REG2	RT5058_REGULATOR_REG2
#define RT5058_REGULATOR_EN_REG3	RT5058_REGULATOR_REG3
#define RT5058_REGULATOR_EN_REG4	RT5058_REGULATOR_REG4
#define RT5058_REGULATOR_EN_REG5	RT5058_REGULATOR_REG5

#define RT5058_BUCK_INDEX_MAX	21
#define RT5058_LDO_INDEX_MAX	19

enum {
	PMICEVENT_BUCK1OCP,
	PMICEVENT_BUCK1LV,
	PMICEVENT_OTP = 5,
	PMICEVENT_VDDAOVP,
	PMICEVENT_VDDAUV,
	PMICEVENT_SLDO2LV,
	PMICEVENT_SLDO1LV,
	PMICEVENT_LDO3LV = 12,
	PMICEVENT_LDO2LV,
	PMICEVENT_LDO1LV,
	PMICEVENT_MAX,
};

#endif /* __LINUX_RT5058_REGULATOR_H */
